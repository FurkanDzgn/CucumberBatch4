package API.PojoExample.Akchabar;

public class Rates {

    private String usd;
    private String euro;
    private String ruble;
    private String tenge;
    private String btc;

    public String getUsd() {
        return usd;
    }

    public void setUsd(String usd) {
        this.usd = usd;
    }

    public String getEuro() {
        return euro;
    }

    public void setEuro(String euro) {
        this.euro = euro;
    }

    public String getRuble() {
        return ruble;
    }

    public void setRuble(String ruble) {
        this.ruble = ruble;
    }

    public String getTenge() {
        return tenge;
    }

    public void setTenge(String tenge) {
        this.tenge = tenge;
    }

    public String getBtc() {
        return btc;
    }

    public void setBtc(String btc) {
        this.btc = btc;
    }
}
